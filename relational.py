# RELATIONAL OPERATORS IN PYTHON
print("=== RELATIONAL OPERATORS ===\n")

# Initialize variables
x = 10
y = 5

print(f"x = {x}, y = {y}\n")

# Equal to
print(f"Equal to: {x} == {y} → {x == y}")

# Not equal to
print(f"Not equal to: {x} != {y} → {x != y}")

# Greater than
print(f"Greater than: {x} > {y} → {x > y}")

# Less than
print(f"Less than: {x} < {y} → {x < y}")

# Greater than or equal to
print(f"Greater than or equal to: {x} >= {y} → {x >= y}")

# Less than or equal to
print(f"Less than or equal to: {x} <= {y} → {x <= y}")

# Additional examples with same values
print(f"\nWhen both are same ({x} == {x}): {x == x}")
print(f"When comparing different types ('10' == 10): {'10' == 10}")